use cryptopals::english_score::single_byte_key_with_best_score;
use cryptopals::prelude::*;

fn main() {
    let oracle = secret::Oracle::new();
    let ciphertexts = oracle.get_ciphertexts();

    let mut probable_keystream = Vec::new();

    for index in 0.. {
        let ciphertext_block: Vec<u8> = ciphertexts
            .iter()
            .filter_map(|ciphertext| ciphertext.get(index))
            .copied()
            .collect();

        if ciphertext_block.len() == 0 {
            break;
        }

        let keystream_byte = single_byte_key_with_best_score(&ciphertext_block);
        probable_keystream.push(keystream_byte);
    }

    println!(
        "Probable keystream block: {}",
        encoding::bytes_to_hex(&probable_keystream)
    );

    for ciphertext in ciphertexts.iter() {
        let applicable_keystream = &probable_keystream[0..ciphertext.len()];

        println!(
            "{:?}",
            String::from_utf8(xor::fixed_xor(&ciphertext, applicable_keystream))
        );
    }
}

